#include <stdio.h>
#include <string.h>
int main()
{
	unsigned int round;
	unsigned short int locate = 0;
	char swap[50];
	scanf("%d", &round);
	while (round > 0)
	{
		strcpy(swap, " ");
		scanf("%s", swap);
		for (int i = 0; i < 50 || swap[i] = ' '; i++)
		{
			switch (locate)
			{
				case 0:
					break;
				case 1:
					break;
				case 2:
					break;
				default:
					break;
			}
		}
		round--;
	}
	return 0;
}
